    private void bBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarActionPerformed
        if (tFichajes.getSelectedRow() != -1) {
            Object idFichaje1 = tFichajes.getValueAt(tFichajes.getSelectedRow(), 0);
            if (controlador.deleteFichaje(Integer.parseInt(idFichaje1.toString()))) {
                JOptionPane.showMessageDialog(this, "El fichaje " + idFichaje1.toString() + " ha sido eliminado correctamente");
                rellenarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al borrar el fichaje", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro para borrarlo", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bBorrarActionPerformed

