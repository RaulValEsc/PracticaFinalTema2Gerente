    private void bBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarActionPerformed
        if (tFestivos.getSelectedRow() != -1) {
            Object fecha = tFestivos.getValueAt(tFestivos.getSelectedRow(), 0);
            java.util.Date fechaAux = null;
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            try {
                fechaAux = formato.parse(fecha.toString());
            } catch (ParseException ex) {
                System.out.println("La fecha no ha podido ser parseada");
            }
            java.sql.Date fecha1 = new java.sql.Date(fechaAux.getTime());
            if (controlador.deleteFestivo(fecha1)) {
                JOptionPane.showMessageDialog(this, "El festivo ha sido eliminado correctamente");
                rellenarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al borrar el festivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro para borrarlo", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bBorrarActionPerformed

    public void rellenarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tFestivos.getModel();
        modelo.setRowCount(0);
        ArrayList<Festivo> lista = controlador.devolverFestivos();
        for (Festivo f : lista) {
            String fechaAux;
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            fechaAux = formato.format(f.getFecha());
            modelo.addRow(new Object[]{fechaAux});
        }
    }
    
