    private void bCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCrearActionPerformed
        if(!etDni.getText().isEmpty()&&!etNombre.getText().isEmpty()){
            if(etHorasMin.getText().isEmpty()&&etPrecioHoras.getText().isEmpty()&&etPrecioHorasEx.getText().isEmpty()){
                if(!con.insertEmpleado(new Empleado(etDni.getText(),etNombre.getText()))){
                    JOptionPane.showMessageDialog(this, "El empleado con dni "+etDni.getText()+" no ha podido ser añadido","Error",JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Alta correcta");
                    etDni.setText("");
                    etNombre.setText("");
                    etHorasMin.setText("");
                    etPrecioHoras.setText("");
                    etPrecioHorasEx.setText("");
                }
            }else if(etPrecioHorasEx.getText().isEmpty()){
                if(!con.insertEmpleado(new Empleado(etDni.getText(),etNombre.getText(), Double.parseDouble(etHorasMin.getText().replace(",",".")), Double.parseDouble(etPrecioHoras.getText().replace(",","."))))){
                    JOptionPane.showMessageDialog(this, "El empleado con dni "+etDni.getText()+" no ha podido ser añadido","Error",JOptionPane.ERROR_MESSAGE);                    
                }else{
                    JOptionPane.showMessageDialog(this, "Alta correcta");
                    etDni.setText("");
                    etNombre.setText("");
                    etHorasMin.setText("");
                    etPrecioHoras.setText("");
                    etPrecioHorasEx.setText("");
                }
            }else{
                if(!con.insertEmpleado(new Empleado(etDni.getText(),etNombre.getText(), Double.parseDouble(etHorasMin.getText().replace(",",".")), Double.parseDouble(etPrecioHoras.getText().replace(",",".")),Double.parseDouble(etPrecioHorasEx.getText().replace(",","."))))){
                    JOptionPane.showMessageDialog(this, "El empleado con dni "+etDni.getText()+" no ha podido ser añadido","Error",JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Alta correcta");
                    etDni.setText("");
                    etNombre.setText("");
                    etHorasMin.setText("");
                    etPrecioHoras.setText("");
                    etPrecioHorasEx.setText("");
                }
            }
        }
    }//GEN-LAST:event_bCrearActionPerformed

