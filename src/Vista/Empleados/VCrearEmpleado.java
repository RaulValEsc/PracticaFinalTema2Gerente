    private void etDniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_etDniKeyTyped
        char car = evt.getKeyChar();
        if (!Character.isDigit(car) && evt.getKeyChar() != KeyEvent.VK_BACK_SPACE && !Character.isAlphabetic(car)) {
            if (etDni.getText().length() == 10) {
                evt.consume();
                getToolkit().beep();
            }
        }
    }//GEN-LAST:event_etDniKeyTyped

    private void etNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_etNombreKeyTyped
        char car = evt.getKeyChar();
        if (evt.getKeyChar() != KeyEvent.VK_BACK_SPACE && !Character.isAlphabetic(car)) {
            if (etNombre.getText().length() == 100) {
                evt.consume();
                getToolkit().beep();
            }
        }
    }//GEN-LAST:event_etNombreKeyTyped

    private void etHorasMinKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_etHorasMinKeyTyped
        char num = evt.getKeyChar();
        if (!Character.isDigit(num) && evt.getKeyChar() != KeyEvent.VK_BACK_SPACE && evt.getKeyChar() != KeyEvent.VK_COMMA) {
            evt.consume();
            getToolkit().beep();
        }
    }//GEN-LAST:event_etHorasMinKeyTyped

